var grade='A';
switch (grade)
{
case 'A': alert("Good job<br />");
break;
case 'B': alert("Pretty good<br />");
break;
case 'C': alert("Passed<br />");
break;
case 'D': alert("Not so good<br />");
break;
case 'F': alert("Failed<br />");
break;
default:alert("Unknown grade<br />")
}
alert("Exiting switch block");